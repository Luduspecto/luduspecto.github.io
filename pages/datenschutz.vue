                wenn eine
                Website im Schneckentempo lädt. Meist verliert man sogar die Geduld und sucht das Weite, bevor die
                Website
                vollständig geladen ist. Das wollen wir natürlich vermeiden. Daher gehört eine schnell ladende Website
                ganz
                selbstverständlich zu unserem Websiteangebot. Mit einem Content Delivery Network wird in Ihrem Browser
                unsere
                Website deutlich schneller geladen. Besonders hilfreich ist der Einsatz des CDNs wenn Sie im Ausland
                sind, weil
                die Website von einem Server in Ihrer Nähe ausgeliefert wird.</p>
