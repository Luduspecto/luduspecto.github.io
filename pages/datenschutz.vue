            <h3 class="text-base">Was sind Cookies?</h3>
            <p>Unsere Website verwendet HTTP-Cookies, um nutzerspezifische Daten zu speichern.<br>
                Im Folgenden erklären wir, was Cookies sind und warum Sie genutzt werden, damit Sie die folgende
                Datenschutzerklärung besser verstehen.</p>
            <p>Immer wenn Sie durch das Internet surfen, verwenden Sie einen Browser. Bekannte Browser sind
                beispielsweise
                Chrome, Safari, Firefox, Internet Explorer und Microsoft Edge. Die meisten Websites speichern kleine
                Text-Dateien in Ihrem Browser. Diese Dateien nennt man Cookies.</p>
            <p>Eines ist nicht von der Hand zu weisen: Cookies sind echt nützliche Helferlein. Fast alle Websites
                verwenden
                Cookies. Genauer gesprochen sind es HTTP-Cookies, da es auch noch andere Cookies für andere
                Anwendungsbereiche
                gibt. HTTP-Cookies sind kleine Dateien, die von unserer Website auf Ihrem Computer gespeichert werden.
                Diese
                Cookie-Dateien werden automatisch im Cookie-Ordner, quasi dem “Hirn” Ihres Browsers, untergebracht. Ein
                Cookie
                besteht aus einem Namen und einem Wert. Bei der Definition eines Cookies müssen zusätzlich ein oder
                mehrere
                Attribute angegeben werden.</p>
            <p>Cookies speichern gewisse Nutzerdaten von Ihnen, wie beispielsweise Sprache oder persönliche
                Seiteneinstellungen.
                Wenn Sie unsere Seite wieder aufrufen, übermittelt Ihr Browser die „userbezogenen“ Informationen an
                unsere Seite
                zurück. Dank der Cookies weiß unsere Website, wer Sie sind und bietet Ihnen die Einstellung, die Sie
                gewohnt
                sind. In einigen Browsern hat jedes Cookie eine eigene Datei, in anderen wie beispielsweise Firefox sind
                alle
                Cookies in einer einzigen Datei gespeichert.</p>
            <p>Die folgende Grafik zeigt eine mögliche Interaktion zwischen einem Webbrowser wie z. B. Chrome und dem
                Webserver.
                Dabei fordert der Webbrowser eine Website an und erhält vom Server ein Cookie zurück, welches der
                Browser erneut
                verwendet, sobald eine andere Seite angefordert wird.</p>
